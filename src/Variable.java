public class Variable {
	public static void main(String[] args) {
		byte core = 8;		
		System.out.println(core);
		short virtualCore = 16;		
		System.out.println(virtualCore);
		int mhz = 3593;
		System.out.println(mhz);
		long memory = 32L;
		System.out.println(memory);
		float allMemory = 32.0F;
		System.out.println(allMemory);
		double availableMemory = 20.4;
		System.out.println(availableMemory);
		char os = 'M';
		System.out.println(os);
		boolean isMyPC = true;
		System.out.println(isMyPC);
		System.out.println("test change");
	}
}